#include "push_swap.h"
int	ft_isdigit(char *s)
{
	int	i;

	i = 0;
	while (s[i] != NULL)
	{
		if (s[i] >= '0' && s[i] <= '9')
			return (0);
		i++;
	}
	return (1);
}
